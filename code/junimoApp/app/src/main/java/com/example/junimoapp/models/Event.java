package com.example.junimoapp.models;
public class Event {

    private String title;
    private String description;
    private String lotteryRules;

    public Event(){}

    public String getTitle() {
        return title;
    }
    public String getDescription() {
        return description;
    }
    public String getLotteryRules() {
        return lotteryRules;
    }
}